namespace UUP\Web\Component\Style;

/**
 * Basic Box Properties (CSS).
 *
 * @author Anders Lövgren (Nowise Systems)
 * @package UUP
 * @subpackage Web Components
 */
class Box
{

        /**
         * Specifies the bottom position of a positioned element. Applies to CSS2.
         */
        const BOTTOM = 'bottom';
        /**
         * Specifies which sides of an element where other floating elements are not allowed. Applies to CSS1.
         */
        const CLEAR = 'clear';
        /**
         * Clips an absolutely positioned element. Applies to CSS2.
         */
        const CLIP = 'clip';
        /**
         * Specifies how a certain HTML element should be displayed. Applies to CSS1.
         */
        const DISPLAY = 'display';
        /**
         * Specifies whether or not a box should float. Applies to CSS1.
         */
        const FLOAT = 'float';
        /**
         * Sets the height of an element. Applies to CSS1.
         */
        const HEIGHT = 'height';
        /**
         * Specifies the left position of a positioned element. Applies to CSS2.
         */
        const LEFT = 'left';
        /**
         * Sets all the margin properties in one declaration. Applies to CSS1.
         */
        const MARGIN = 'margin';
        /**
         * Sets the bottom margin of an element. Applies to CSS1.
         */
        const MARGIN_BOTTOM = 'margin-bottom';
        /**
         * Sets the left margin of an element. Applies to CSS1.
         */
        const MARGIN_LEFT = 'margin-left';
        /**
         * Sets the right margin of an element. Applies to CSS1.
         */
        const MARGIN_RIGHT = 'margin-right';
        /**
         * Sets the top margin of an element. Applies to CSS1.
         */
        const MARGIN_TOP = 'margin-top';
        /**
         * Sets the maximum height of an element. Applies to CSS2.
         */
        const MAX_HEIGHT = 'max-height';
        /**
         * Sets the maximum width of an element. Applies to CSS2.
         */
        const MAX_WIDTH = 'max-width';
        /**
         * Sets the minimum height of an element. Applies to CSS2.
         */
        const MIN_HEIGHT = 'min-height';
        /**
         * Sets the minimum width of an element. Applies to CSS2.
         */
        const MIN_WIDTH = 'min-width';
        /**
         * Specifies what happens if content overflows an element's box. Applies to CSS2.
         */
        const OVERFLOW = 'overflow';
        /**
         * Specifies whether or not to clip the left/right edges of the content, if it overflows the element's content area. Applies to CSS3.
         */
        const OVERFLOW_X = 'overflow-x';
        /**
         * Specifies whether or not to clip the top/bottom edges of the content, if it overflows the element's content area. Applies to CSS3.
         */
        const OVERFLOW_Y = 'overflow-y';
        /**
         * Sets all the padding properties in one declaration. Applies to CSS1.
         */
        const PADDING = 'padding';
        /**
         * Sets the bottom padding of an element. Applies to CSS1.
         */
        const PADDING_BOTTOM = 'padding-bottom';
        /**
         * Sets the left padding of an element. Applies to CSS1.
         */
        const PADDING_LEFT = 'padding-left';
        /**
         * Sets the right padding of an element. Applies to CSS1.
         */
        const PADDING_RIGHT = 'padding-right';
        /**
         * Sets the top padding of an element. Applies to CSS1.
         */
        const PADDING_TOP = 'padding-top';
        /**
         * Specifies the type of positioning method used for an element (static, relative, absolute or fixed). Applies to CSS2.
         */
        const POSITION = 'position';
        /**
         * Specifies the right position of a positioned element. Applies to CSS2.
         */
        const RIGHT = 'right';
        /**
         * Specifies the top position of a positioned element. Applies to CSS2.
         */
        const TOP = 'top';
        /**
         * Specifies whether or not an element is visible. Applies to CSS2.
         */
        const VISIBILITY = 'visibility';
        /**
         * Sets the width of an element. Applies to CSS1.
         */
        const WIDTH = 'width';
        /**
         * Sets the vertical alignment of an element. Applies to CSS1.
         */
        const VERTICAL_ALIGN = 'vertical-align';
        /**
         * Sets the stack order of a positioned element. Applies to CSS2.
         */
        const Z_INDEX = 'z-index';

}
